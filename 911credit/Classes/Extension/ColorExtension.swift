import Foundation
import UIKit

extension UIColor {
    
    class func getBlueColor() -> UIColor {
        return UIColor(red: 0, green: 83, blue: 137, alpha: 1.00)
    }
    
    class func getRedColor() -> UIColor {
        return UIColor(red: 201, green: 59, blue: 43, alpha: 1.00)
    }
    
    class func getGrayColor() -> UIColor {
        return UIColor(red: 165, green: 165, blue: 165, alpha: 1.00)
    }
    
    class func getBlackColor() -> UIColor {
        return UIColor(red: 51, green: 51, blue: 51, alpha: 1.00)
    }
    
    class func getWhiteColor() -> UIColor {
        return UIColor(red: 255, green: 255, blue: 255, alpha: 1.00)
    }
    
}
